package com.best1.scm.modules.outinterface.entity;

import java.io.Serializable;

/**
 * 执行结果json对应的Model对象
 * @author zhangzujie
 *
 */
public class JsonResultModel implements Serializable{

	private int isSuccess; //是否成功,1代表成功,0代表失败
	private String key  ;//执行单号记录对应的key
	private String failureType ;//失败类型
	private String failureDesc;//失败具体描述
	
	public int getIsSuccess() {
		return isSuccess;
	}
	public void setIsSuccess(int isSuccess) {
		this.isSuccess = isSuccess;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getFailureType() {
		return failureType;
	}
	public void setFailureType(String failureType) {
		this.failureType = failureType;
	}
	public String getFailureDesc() {
		return failureDesc;
	}
	public void setFailureDesc(String failureDesc) {
		this.failureDesc = failureDesc;
	}
}
